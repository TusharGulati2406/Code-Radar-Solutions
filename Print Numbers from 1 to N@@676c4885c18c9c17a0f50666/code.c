#include <stdio.h>

int main() {
    int N;
    
    // Input the number N
   
    scanf("%d", &N);
    
    // Loop to print numbers from 1 to N
    for (int i = 1; i <= N; i++) {
        printf("%d ", i);  // Print each number followed by a space
    }
    
   
    
    return 0;
}
